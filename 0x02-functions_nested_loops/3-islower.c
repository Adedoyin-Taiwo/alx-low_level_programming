/**
* _islower - the function returns if charcter is lowercase
* Return: 0 if false, 1 if true
* @c: this is the test character
* _islower -  the function returns if the character is lowercase
*/

int _islower(int c)
{
	if (c >= 97 && c <= 122)
	{
		return (1);
	}
	else
	{
		return (0);
	}
	return (0);
}


